function displayDescriptionCovid() {
	document.getElementById("proj-desc-covid").style.display = "block";
	document.getElementById("proj-desc-paradise").style.display = "none";
	document.getElementById("proj-desc-overthemoon").style.display = "none";
}
function displayDescriptionParadise() {
	document.getElementById("proj-desc-covid").style.display = "none";
	document.getElementById("proj-desc-paradise").style.display = "block";
	document.getElementById("proj-desc-overthemoon").style.display = "none";
}
function displayDescriptionOverTheMoon() {
	document.getElementById("proj-desc-covid").style.display = "none";
	document.getElementById("proj-desc-paradise").style.display = "none";
	document.getElementById("proj-desc-overthemoon").style.display = "block";
}